package com.tangotab.dao.pojo;

import java.sql.Timestamp;


public class HomeSignUp implements java.io.Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private Integer homeId;
	private String emailId;
	private Timestamp signupDate;
	private String preferCity;
	public Integer getHomeId() {
		return homeId;
	}
	public void setHomeId(Integer homeId) {
		this.homeId = homeId;
	}
	public String getEmailId() {
		return emailId;
	}
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	public Timestamp getSignupDate() {
		return signupDate;
	}
	public void setSignupDate(Timestamp signupDate) {
		this.signupDate = signupDate;
	}
	public String getPreferCity() {
		return preferCity;
	}
	public void setPreferCity(String preferCity) {
		this.preferCity = preferCity;
	}
	
	

	}
