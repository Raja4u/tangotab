package com.tangotab.dao.pojo;

public class AdminDealImages implements java.io.Serializable {
	private static final long serialVersionUID = 1L;
	private Integer dealImagId;
	private String dealImgPath;
	private DealName dealName;

	public AdminDealImages() {
	}

	public AdminDealImages(String dealImgPath) {
		this.dealImgPath = dealImgPath;
	}

	public Integer getDealImagId() {
		return this.dealImagId;
	}

	public void setDealImagId(Integer dealImagId) {
		this.dealImagId = dealImagId;
	}

	public String getDealImgPath() {
		return this.dealImgPath;
	}

	public void setDealImgPath(String dealImgPath) {
		this.dealImgPath = dealImgPath;
	}

	public void setDealName(DealName dealName) {
		this.dealName = dealName;
	}

	public DealName getDealName() {
		return dealName;
	}

}