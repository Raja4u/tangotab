package com.tangotab.web.form;

import org.apache.struts.validator.ValidatorForm;

public class DiningStyleForm extends ValidatorForm implements java.io.Serializable {

	private Integer diningStyleId;
	private String diningStyleName;
	private Integer isActive;

	/**
	 * @return the diningStyleId
	 */
	public Integer getDiningStyleId() {
		return diningStyleId;
	}

	/**
	 * @param diningStyleId
	 *            the diningStyleId to set
	 */
	public void setDiningStyleId(Integer diningStyleId) {
		this.diningStyleId = diningStyleId;
	}

	/**
	 * @return the diningStyleName
	 */
	public String getDiningStyleName() {
		return diningStyleName;
	}

	/**
	 * @param diningStyleName
	 *            the diningStyleName to set
	 */
	public void setDiningStyleName(String diningStyleName) {
		this.diningStyleName = diningStyleName;
	}

	/**
	 * @return the isActive
	 */
	public Integer getIsActive() {
		return isActive;
	}

	/**
	 * @param isActive
	 *            the isActive to set
	 */
	public void setIsActive(Integer isActive) {
		this.isActive = isActive;
	}

}
