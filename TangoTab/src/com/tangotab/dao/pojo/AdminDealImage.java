package com.tangotab.dao.pojo;

public class AdminDealImage implements java.io.Serializable {

	private static final long serialVersionUID = 1L;
	private Integer imageId;
	private String imagePath;
	private DealName dealName;

	public AdminDealImage() {
	}

	public AdminDealImage(String imagePath, Integer imageId, DealName dealName) {
		this.imagePath = imagePath;
		this.imageId = imageId;
		this.dealName = dealName;
	}

	public DealName getDealName() {
		return dealName;
	}

	public void setDealName(DealName dealName) {
		this.dealName = dealName;
	}

	public Integer getImageId() {
		return imageId;
	}

	public void setImageId(Integer imageId) {
		this.imageId = imageId;
	}

	public String getImagePath() {
		return imagePath;
	}

	public void setImagePath(String imagePath) {
		this.imagePath = imagePath;
	}

}
