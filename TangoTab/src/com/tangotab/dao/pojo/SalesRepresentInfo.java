package com.tangotab.dao.pojo;

import java.util.HashSet;
import java.util.Set;

public class SalesRepresentInfo implements java.io.Serializable {
	private static final long serialVersionUID = 1L;
	private Integer salesRepId;
	private User user;
	private SalesInfo salesInfo;
	private SalesManagerInfo assignedManger;

	public SalesManagerInfo getAssignedManger() {
		return assignedManger;
	}

	public void setAssignedManger(SalesManagerInfo assignedManger) {
		this.assignedManger = assignedManger;
	}

	private Set<SalesRepZipcodes> salesRepZipcodeses = new HashSet<SalesRepZipcodes>();

	public Integer getSalesRepId() {
		return salesRepId;
	}

	public void setSalesRepId(Integer salesRepId) {
		this.salesRepId = salesRepId;
	}

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	public SalesInfo getSalesInfo() {
		return salesInfo;
	}

	public void setSalesInfo(SalesInfo salesInfo) {
		this.salesInfo = salesInfo;
	}

	public Set<SalesRepZipcodes> getSalesRepZipcodeses() {
		return salesRepZipcodeses;
	}

	public void setSalesRepZipcodeses(Set<SalesRepZipcodes> salesRepZipcodeses) {
		this.salesRepZipcodeses = salesRepZipcodeses;
	}

}