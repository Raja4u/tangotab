package com.tangotab.dao.pojo;

import java.sql.Timestamp;

public class CharityDetails implements java.io.Serializable {
	private static final long serialVersionUID = 1L;
	private Integer charityId;
	private Timestamp charityTime;
	private Integer totalCount;
	
	public Integer getCharityId() {
		return charityId;
	}
	public void setCharityId(Integer charityId) {
		this.charityId = charityId;
	}
	public Timestamp getCharityTime() {
		return charityTime;
	}
	public void setCharityTime(Timestamp charityTime) {
		this.charityTime = charityTime;
	}
	public Integer getTotalCount() {
		return totalCount;
	}
	public void setTotalCount(Integer totalCount) {
		this.totalCount = totalCount;
	}
}
