package com.tangotab.dao.pojo;

import java.util.HashSet;
import java.util.Set;

public class RestaurantType implements java.io.Serializable {
	private static final long serialVersionUID = 1L;
	private Integer restTypeId;
	private String restTypeName;
	private Set<Restaurant> restaurants = new HashSet<Restaurant>(0);
	private Integer isActive;

	/**
	 * @return the isActive
	 */
	public Integer getIsActive() {
		return isActive;
	}

	/**
	 * @param isActive
	 *            the isActive to set
	 */
	public void setIsActive(Integer isActive) {
		this.isActive = isActive;
	}

	public Integer getRestTypeId() {
		return restTypeId;
	}

	public void setRestTypeId(Integer restTypeId) {
		this.restTypeId = restTypeId;
	}

	public String getRestTypeName() {
		return restTypeName;
	}

	public void setRestTypeName(String restTypeName) {
		this.restTypeName = restTypeName;
	}

	public Set<Restaurant> getRestaurants() {
		return restaurants;
	}

	public void setRestaurants(Set<Restaurant> restaurants) {
		this.restaurants = restaurants;
	}

}